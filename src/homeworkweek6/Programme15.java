package homeworkweek6;

/**
 * Write a Java program to swap two variables
 */
public class Programme15 {
    public static void main(String[] args) {
        int a, b;
         a = 15;
         b = 27;
         int temp;
        System.out.println("Before swapping : " + a + "," + + b);
        temp = a;
         a = b;
         b = temp;
        System.out.println("After swapping : " + a + "," + + b);
    }


}
